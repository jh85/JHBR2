/*
  JHBR2 — Move generation correctness test

  Reads test data generated by test/generate_positions.py (cshogi oracle).
  For each position, compares GenerateLegalMoves() output against expected.

  Usage:
    ./test_movegen test/positions.txt
*/

#include <algorithm>
#include <cstdlib>
#include <fstream>
#include <iostream>
#include <set>
#include <sstream>
#include <string>
#include <vector>

#include "shogi/bitboard.h"
#include "shogi/board.h"
#include "shogi/types.h"

using namespace lczero;

struct TestCase {
  std::string sfen;
  std::set<std::string> expected_moves;
};

static std::vector<TestCase> LoadTestData(const std::string& path) {
  std::vector<TestCase> tests;
  std::ifstream fin(path);
  if (!fin.is_open()) {
    std::cerr << "ERROR: Cannot open " << path << std::endl;
    std::exit(1);
  }

  std::string line;
  int line_num = 0;
  while (std::getline(fin, line)) {
    line_num++;
    if (line.empty() || line[0] == '#') continue;

    // Format: SFEN<TAB>move1 move2 ...
    auto tab = line.find('\t');
    if (tab == std::string::npos) {
      std::cerr << "WARNING: No tab on line " << line_num
                << ", skipping" << std::endl;
      continue;
    }

    TestCase tc;
    tc.sfen = line.substr(0, tab);
    std::string moves_str = line.substr(tab + 1);

    std::istringstream mss(moves_str);
    std::string m;
    while (mss >> m) {
      tc.expected_moves.insert(m);
    }
    tests.push_back(std::move(tc));
  }
  return tests;
}

int main(int argc, char* argv[]) {
  if (argc < 2) {
    std::cerr << "Usage: " << argv[0] << " <positions.txt>" << std::endl;
    return 1;
  }

  // Initialize tables.
  ShogiTables::Init();

  auto tests = LoadTestData(argv[1]);
  std::cout << "Loaded " << tests.size() << " test positions" << std::endl;

  int passed = 0;
  int failed = 0;
  int parse_errors = 0;
  constexpr int kMaxFailDetails = 50;

  for (size_t i = 0; i < tests.size(); i++) {
    const auto& tc = tests[i];

    ShogiBoard board;
    if (!board.SetFromSfen(tc.sfen)) {
      std::cerr << "PARSE ERROR [" << i + 1 << "]: " << tc.sfen << std::endl;
      parse_errors++;
      continue;
    }

    // Generate legal moves.
    MoveList legal = board.GenerateLegalMoves();
    std::set<std::string> got_moves;
    for (const Move& m : legal) {
      got_moves.insert(m.ToString());
    }

    if (got_moves == tc.expected_moves) {
      passed++;
      continue;
    }

    // Mismatch — report details.
    failed++;
    if (failed <= kMaxFailDetails) {
      std::cerr << "FAIL [" << i + 1 << "] SFEN: " << tc.sfen << std::endl;

      // Moves in expected but not in jhbr2 (missing).
      std::vector<std::string> missing;
      std::set_difference(tc.expected_moves.begin(), tc.expected_moves.end(),
                          got_moves.begin(), got_moves.end(),
                          std::back_inserter(missing));

      // Moves in jhbr2 but not in expected (extra/illegal).
      std::vector<std::string> extra;
      std::set_difference(got_moves.begin(), got_moves.end(),
                          tc.expected_moves.begin(), tc.expected_moves.end(),
                          std::back_inserter(extra));

      if (!missing.empty()) {
        std::cerr << "  MISSING (" << missing.size() << "):";
        for (const auto& m : missing) std::cerr << " " << m;
        std::cerr << std::endl;
      }
      if (!extra.empty()) {
        std::cerr << "  EXTRA (" << extra.size() << "):";
        for (const auto& m : extra) std::cerr << " " << m;
        std::cerr << std::endl;
      }

      std::cerr << "  expected " << tc.expected_moves.size()
                << " moves, got " << got_moves.size() << std::endl;
    }
  }

  // Summary.
  std::cout << "\n=== RESULTS ===" << std::endl;
  std::cout << "Passed:       " << passed << " / " << tests.size() << std::endl;
  if (parse_errors > 0)
    std::cout << "Parse errors: " << parse_errors << std::endl;
  if (failed > 0) {
    std::cout << "FAILED:       " << failed << std::endl;
    if (failed > kMaxFailDetails)
      std::cout << "(showing first " << kMaxFailDetails << " failures)" << std::endl;
    return 1;
  }

  std::cout << "ALL PASSED" << std::endl;
  return 0;
}
